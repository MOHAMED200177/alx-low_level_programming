#include "main.h"
#include <stdio.h>

/**
 * leet - encodes a string into 1337.
 *@str: input
 * Return: Always 0.
 */
char *leet(char *str) {
char *s = str;
while (*s) {
if (*s == 'a' || *s == 'A') {
*s = '4';
} else if (*s == 'e' || *s == 'E') {
*s = '3';
} else if (*s == 'o' || *s == 'O') {
*s = '0';
} else if (*s == 't' || *s == 'T') {
*s = '7';
} else if (*s == 'l' || *s == 'L') {
*s = '1';
}
s++;
}
return str;
}
